async function main() {
  const guidePath = path.join(process.cwd(), "data", "guide", "housing-rental-guide.json");
  const raw = await readFile(guidePath, "utf-8");
  const data = lawGuideDatasetSchema.parse(JSON.parse(raw));

  const missing: string[] = [];
  for (const a of data.articles) {
    const text = EASY_BY_ARTICLE[a.articleNo];
    if (!text) missing.push(a.articleNo);
    else if (a.lines[0]) a.lines[0].easyInterpretation = text;
  }
  if (missing.length) {
    console.error("요약 문구가 없는 조:", missing.join(", "));
    process.exit(1);
  }

  data.disclaimer =
    "이 페이지의 원문은 국가법령정보센터 등에서 옮긴 것입니다. 아래 쉬운 해설은 일반인의 이해를 돕기 위한 요약이며 법률 자문이 아닙니다. 실제 적용은 최신 공포 본문·시행령·판례와 개별 사실관계에 따라 달라질 수 있습니다.";

  await writeFile(guidePath, JSON.stringify(data, null, 2), "utf-8");
  console.log("갱신:", guidePath, "조문 수:", data.articles.length);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
